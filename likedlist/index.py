arr=[1, 5, 8, 10]
k=2
def small_item(arr,k):
    small;
    arr1;
    for i in range(len(arr)):
        if arr==1:
            return
        if k>arr[i]:
          arr1=arr[i].value+k
        else:
          arr1=arr[i].value-k
          
    min=min(arr1)
    max=max(arr1)
    small=max-min
    return small
    
    
